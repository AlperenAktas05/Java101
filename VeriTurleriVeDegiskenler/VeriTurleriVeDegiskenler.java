
public class VeriTurleriVeDegiskenler {
	public static void main(StringSinifi[] args) {
		
		byte b=1;  //1 byte
		short s=10;  //2 byte
		int i=100;  //4 byte
		long l=1000;  //8 byte
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		
		float f=3.14f;  //4 byte
		double d=5.47;  //8 byte
		
		System.out.println(f);
		System.out.println(d);
		
		char c='c';  //2 byte
		
		System.out.println(c);
		
		boolean control=true;  //1 byte
		
		System.out.println(control);
		
		//ayni isimde 2 veri tipi olamaz
		//veri tipleri sayi ile baslayamaz
		//veri tiplerine deger vermeden calıstırılamaz
		
	}
}
